# Arithmetic Operators
a = 10
b = 3
print("# Arithmetic Operators")
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

print("# Comparison / Relational Operators")
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

print("# Logical Operators")
x = True
y = False
print(x and y)
print(x or y)
print(not x)

print("# Assignment Operators")
c = 5
c += 2
print(c)
c *= 3
print(c)

print("# Bitwise Operators")
x = 5
y = 3
print(x & y)
print(x | y)
print(x ^ y)
print(~x)
print(x << 1)
print(x >> 1)

print("# Identity Operators")
a = [1, 2]
b = a
c = [1, 2]
print(a is b)
print(a is c)
print(a is not c)

print("# Membership Operators")
my_list = [1, 2, 3]
print(2 in my_list)
print(4 not in my_list)
